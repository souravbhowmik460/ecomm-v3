    /* For Search Modal */
    document.addEventListener("DOMContentLoaded", function() {
        const searchInput = document.querySelector(".searchcontent"); // Updated class name
        const searchList = document.querySelector(".search_lists");

        if (searchInput && searchList) { // Ensure elements exist
            searchInput.addEventListener("focus", function() {
                searchList.classList.add("active");
            });

            searchInput.addEventListener("blur", function() {
                setTimeout(() => {
                    searchList.classList.remove("active");
                }, 200); // Delay to allow clicking list items
            });
        } else {
            console.error("Search input or list not found!");
        }
    });

    
</script>